function buildSeedRows() {
  const rows = [];
  let i = 0;
  FINISH_TEMPLATE.forEach((r) => {
    rows.push({
      kind: 'FINISH',
      spaceGroup: r.sg,
      itemName: r.it,
      defaultSiteNotes: r.notes || null,
      orderIndex: i++,
    });
  });
  APPLIANCE_TEMPLATE.forEach((r) => {
    rows.push({
      kind: 'APPLIANCE',
      spaceGroup: r.sg,
      itemName: r.it,
      defaultSiteNotes: r.notes || null,
      orderIndex: i++,
    });
  });
  return rows;
}

